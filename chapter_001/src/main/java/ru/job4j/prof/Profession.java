package ru.job4j.prof;

public class Profession {

    private String name;
    private String surname;
    private String education;
    private int age;

    public String getSurname() {
        return surname;
    }

    public String getEducation() {
        return education;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }
}
